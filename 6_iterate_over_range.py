# How do you use a for loop to iterate over a range of numbers in Python?
# Iterate over a range of numbers from 0 to 9
for num in range(10):
    print(num)

# Iterate over a range of numbers from 1 to 10
for num in range(1, 11):
    print(num)

# Iterate over a range of numbers from 1 to 10 with a step of 2
for num in range(1, 11, 2):
    print(num)
