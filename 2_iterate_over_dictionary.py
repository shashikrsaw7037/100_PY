# How to iterate over a dictionary in Python?
my_dict = {'a': 1, 'b': 2, 'c': 3}

# Iterate over keys
for key in my_dict:
    print(key)
print()
# Iterate over values
for value in my_dict.values():
    print(value)
print()
# Iterate over key-value pairs (items)
for key, value in my_dict.items():
    print(key, value)
